<div class="how">
    <h3>How To use</h3>
    <span>To use the plugin you can simply add a short code with the below structure to any place of your website (Pages or Posts). Or you can simply use the WP Bakery Visual Composer Element included.</span>
    <span><br><br>
       
       <b>Short Code Example</b>  <code>[pfbk_pdf_flipbook <b>src</b>="http://www.domain.com/file.pdf" <b>width</b>="700" <b>height</b>="500" <b>pages</b>="on" animation="on"]</code>
    </span>
    <b>Features:</b>
    <ul>
        <li><b>WP Bakery Visual Composer Element (Included):</b> This plugin will automatically add a new element to WP Bakery Visual Composer if you have Visual Composer Installed and activated.</li>
        <li><b> Shortcode Options:</b> This plugin will give you a set of options to enable/disable within the shortcode or from the Visual Composer Element.</li> 
    </ul>
    <b>Shortcode Options:</b>
    <ul>
        <li><b>src</b> is the url of the PDF file (REQUIRED), all the other attributes are optional</li>
        <li><b>width </b>is the width of the PDF file to be displayed</li>
        <li><b>height</b> is the Height of the PDF file to be diplayed</li>
        <li><b>pages</b> attribute (on) for showing the PDF file pages number, (off) for not showing it.</li>
        <li><b>animation="on"</b> attribute (on) for showing the PDF flip pages r, (off) for not showing it.</li>
        <li>You can simply remove the attriputes to turn it off. </li> 
    </ul>
</div>
